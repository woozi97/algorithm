package _1_Student_ArrayList_search;

public class Student {
	private String name;
	private String no;
	
	//생성자를 받아 멤버변수 초기화가능	
	public Student(String name, String no) {
		super();
		this.name = name;
		this.no = no;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	
	
	
	
}
